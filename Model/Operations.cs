namespace EM3.Model
{
    static class Operations
    {
        public static double Sum(double var1, double var2)
        {
            return var1 + var2;
        }

        public static double Subtr(double var1, double var2)
        {
            return var1 - var2;
        }

        public static string Out(double value)
        {
            return value.ToString();
        }
    }
}
